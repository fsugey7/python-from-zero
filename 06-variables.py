# Vimos que a los valores se les puede asignar un nombre. Este nombre se conoce como variable.
# Tal como su nombre lo indica, podemos utilizar el mismo nombre para apuntar a diferentes valores, uno a la vez.

# Cuando primero le damos un valor a una variabe lo llamamos ASIGNACIÓN
name = "mavis"

print(name.title())

# Cando reutilizamos la variable con un nuevo valor se llama REASIGNACIÓN
name = "mavis emilia"

print(name.title())

# Los nombres de las varables son sensibles a las mayusculas y minúsculas:

NAME =  "Soy Más"
Name = "Python"

print(name,NAME,Name)

